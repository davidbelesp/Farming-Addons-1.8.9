package chat;

public class Formatting {

	private static final String BASE = "\u00A7";
	
	
	public static final String BLACK = BASE + "0";
	public static final String RED = BASE + "c";
	public static final String LIME = BASE + "a";
	public static final String AQUA = BASE + "b";
	public static final String WHITE = BASE + "f";
	
	public static final String RESET = BASE + "r";
	public static final String BOLD = BASE + "l";
}
